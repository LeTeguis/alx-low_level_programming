#include "3-calc.h"

/**
 * op_add - sum
 *
 * @a: first
 * @b: second
 *
 * Description: calcul sum of a and b
 *
 * Return: sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - difference
 *
 * @a: first
 * @b: second
 *
 * Description: calcul difference of a and b
 *
 * Return: substraction of a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplication
 *
 * @a: first
 * @b: second
 *
 * Description: calcul multiplication of a and b
 *
 * Return: multiplication of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division
 *
 * @a: first
 * @b: second
 *
 * Description: calcul division of a and b
 *
 * Return: quotiens of a and b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulus
 *
 * @a: first
 * @b: second
 *
 * Description: calcul modulus of a and b
 *
 * Return: modulus of a and b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
